void Astar(country* c,int adj[][20][2],int* visited)
{
    cout<<"\n\n ************************************************** \n\n";
    queue<int> q;
    q.push(0);
    visited[0]=1;
    cout<<c[0].name<<" -> ";
    int u;
    int cost,next,min;

    while(!q.empty())
    {
        u = q.front();
        q.pop();
        min=1000;
        for (int i = 0; i < 20 ; i++)
        {
            if ((adj[u][i][0] == 1) && (visited[i] == 0))
            {
                cost=adj[u][i][1]+c[i].svalue;
                if(cost<min)
                {
                    next=i;
                    min=cost;

                }
            }

        }
        q.push(next);
        cout<<"("<<min<<")"<<c[next].name<<" -> ";
        if(next == 1)
            break;
    }
}

